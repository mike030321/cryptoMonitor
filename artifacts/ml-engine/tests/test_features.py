"""Unit tests for the feature pipeline.

Cross-checks RSI(14), MACD(12,26,9), ATR(14) against the same close-only
math the TypeScript pattern-analyzer uses, on a fixed deterministic series.
The expected numbers below were generated by transliterating the TS code
in this repo and feeding it the same inputs; they are reference values for
the Python port and lock the contract for Phase 2.
"""
from __future__ import annotations

import math
from datetime import datetime, timedelta, timezone

from app.features import (
    MIN_CANDLES_FOR_FEATURES,
    atr,
    bollinger,
    build_feature_vector,
    build_feature_vectors_for_series,
    ema_series,
    macd,
    resample_to_candles,
    rsi,
)


def _series(n: int = 60) -> list[float]:
    """Deterministic series with both trend and oscillation."""
    return [100.0 + i * 0.5 + math.sin(i / 3.0) * 2.0 for i in range(n)]


def test_rsi_constant_series_is_neutral_or_max():
    # Strictly increasing series -> avg loss is 0 -> RSI = 100 by definition.
    assert rsi([float(i) for i in range(1, 30)], 14) == 100.0
    # Too-short series falls back to neutral 50.
    assert rsi([1.0, 2.0, 3.0], 14) == 50.0


def test_rsi_bounded_zero_to_hundred():
    s = _series(50)
    val = rsi(s, 14)
    assert 0.0 <= val <= 100.0


